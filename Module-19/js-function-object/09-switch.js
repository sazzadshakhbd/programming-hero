const color = 'yellow';

if (color === 'green') {
    console.log('green');
} else if (color === 'blue') {
    console.log('blue');
} else if (color === 'red') {
    console.log('red');
} else if (color === 'white') {
    console.log('white');
} else if (color === 'yellow') {
    console.log('yellow')
} else {
    console.log('black')
}

